public class FOR_Impares {
    
    public static void main(String[] args) {
        
        
        
        
        
        System.out.println("Números ímpares de 9 a 1!");
        
        
        for(int c = 9; c >= 1; c--){
            
            int mod = c % 2;
            
            if (mod == 1){
                
                System.out.println(c);
                
                
            }
            
            
        }
        
        
        
    }
    
    

}
